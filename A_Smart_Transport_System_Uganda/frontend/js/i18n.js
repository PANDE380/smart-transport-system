// ── Translation engine ──────────────────────────────────────────────────────
function stsT(key) {
  const lang = (typeof appPrefs !== 'undefined' && appPrefs.language) ? appPrefs.language : 'en';
  return (STS_TRANSLATIONS[lang] && STS_TRANSLATIONS[lang][key]) || STS_TRANSLATIONS.en[key] || key;
}

function stsApplyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = stsT(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      if (el.dataset.i18nAttr === 'placeholder') el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = stsT(el.dataset.i18nPh);
  });
}

// ── Run immediately (i18n.js loads after app.js, DOM is ready) ───────────────
(function init() {
  // Apply translations using the already-loaded appPrefs language
  stsApplyTranslations();

  // Inject selected-lang style
  const style = document.createElement('style');
  style.innerHTML = `.selected-lang{background:#123a5a!important;color:#fff!important;border-radius:8px;border:none;}`;
  document.head.appendChild(style);

  // Make chatbot draggable
  makeChatbotDraggable();
})();

// ── Draggable chatbot ────────────────────────────────────────────────────────
function makeChatbotDraggable() {
  const widget = document.getElementById('chatbot-widget');
  if (!widget) return;
  let dragging = false, ox = 0, oy = 0;
  widget.style.position = 'fixed'; widget.style.zIndex = 9999;
  widget.onmousedown = e => {
    if (e.target.closest('.chatbot-header') || e.target.closest('.chatbot-toggle-btn')) {
      dragging = true;
      ox = e.clientX - widget.getBoundingClientRect().left;
      oy = e.clientY - widget.getBoundingClientRect().top;
      document.body.style.userSelect = 'none';
    }
  };
  document.onmousemove = e => {
    if (dragging) { widget.style.left = (e.clientX - ox) + 'px'; widget.style.top = (e.clientY - oy) + 'px'; }
  };
  document.onmouseup = () => { dragging = false; document.body.style.userSelect = ''; };
}
